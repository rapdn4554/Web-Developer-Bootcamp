// ARRAY METHODS

const array1 = ['a', 'b', 'c'];
const array2 = ['d', 'e', 'f'];

// INCLUDES
// is used to verify if some value is on our array
array1.includes('c') // true

// REVERSE
// is going to reverse our original array's line, so it's destructive
array1.reverse() // array1 = ['c', 'b', 'a'];