// ARRAY METHODS // 

let movieLine = ["lana", "jena"]; // "lana", "jena"


// PUSH
// we can use the push to add a value on a method and the array will be change
// sintax: [name of array].push(argument)
movieLine.push('jeremy') // movieLine = ["lana", "jena", "jeremy"];

// POP
// is going to delete the last value of the array
movieLine.pop(); // movieLine = ["lana", "jena"];