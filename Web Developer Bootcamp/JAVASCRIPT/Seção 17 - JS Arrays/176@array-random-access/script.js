// ARRAY RANDOM ACCESS //

// INDEX
// the elemens into a array are indexed, like the strings
let strings = ['hey', 'hi', 'hello'];

"hey" // 0
"hi" // 1
"hello" // 2

// we can use it to access the values of variable
strings[1] // "hi"
strings[2][4] // 'o'

// CHANGING THE INDEX
// we can change the value of a array, different from strings
strings[0] = 2; // strings = [2, 'hi', 'hello']
