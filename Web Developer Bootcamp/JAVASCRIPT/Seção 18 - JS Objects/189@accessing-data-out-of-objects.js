// ACCESSING DATA OUT OF OBJECTS //

const user = {
    firstName   : 'Daniel',
    secondName  : 'Nunes',
    age         : 16,
    height      : 155
}

// the name to call some value, like "age", in the exemple, would be a key to access it
// there's two options to access our data of objects:

// BRACKETS AND QUOTATION MARKS
user["firstName"] // 'Daniel'

// DOT SINTAX
user.firstName // 'Daniel"

// every key is turned by a string
