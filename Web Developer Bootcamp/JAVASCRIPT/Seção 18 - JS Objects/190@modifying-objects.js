// MODIFYING OBJECTS //

const testsResults = {
    davie       : 5,
    gabriel     : 2,
    anne        : 9
}

// we can change the values of our object elements
testsResults.gabriel = 3; // testsResults {davie:5, gabriel:3, anne:9}

// we can add terms in there too
testsResults.ezra = 'A+'; // testsResults {davie:5, gabriel:3, anne:9, ezra:A+}
testsResults.thomas = 'C'; // testsResults {davie:5, gabriel:3, anne:9, ezra:A+, thomas:C}