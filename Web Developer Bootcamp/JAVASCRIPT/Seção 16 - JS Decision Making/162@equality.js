// COMPARISONS //

// EQUALITY ==
// it doesn't matter if we're working with the same type
"2 == 3" // false

"0 == false" // true

"9 == '9'" // true

"null == undefined" // true

// NOT EQUAL !=
// it doesn't matter if we're working with the same type
"2 != 3" // true

"'a' != 'b'" // true

"1 != 1" // false

// STRICT EQUALITY ===
// it matter the type of our two elements
"2 === 3" // false

"0 === false" // false

"'a' === 40" // false

// STRICT NON-EQUALITY !==
// it matter the type of our two elements
"1 !== 2" // true

"1 !== 1" // false

"null !== undefined" // true